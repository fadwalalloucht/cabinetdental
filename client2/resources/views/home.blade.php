		<div class="faq-item">
			<div class="faq-header">
				<h3 class="faq-question">Est ce que le détartrage fragilise les dents ?</h3>
				<span class="faq-toggle">+</span>
			</div>
			<p class="faq-answer">Le détartrage dentaire est généralement considéré comme sûr et bénéfique pour la santé
				bucco-dentaire. En fait, il est recommandé de subir un détartrage régulier chez le dentiste pour prévenir
				les problèmes dentaires tels que la gingivite (inflammation des gencives) et la maladie parodontale
				(infection des tissus de soutien des dents). Le détartrage peut sembler agressif, car il implique
				l'utilisation d'instruments spéciaux, tels qu'un grattoir ou une sonde ultrasonique, pour enlever le tartre.
				Cependant, lorsqu’il est réalisé par un professionnel qualifié, le détartrage est généralement sûr et ne
				fragilise pas les dents. Au contraire, il permet de maintenir une bonne santé bucco-dentaire en éliminant
				les accumulations indésirables. Il est possible que certaines personnes ressentent une légère sensibilité
				dentaire après un détartrage, mais cela est généralement temporaire et disparaît rapidement.</p>
		</div>

		<div class="faq-item">
			<div class="faq-header">
				<h3 class="faq-question">Est ce que le blanchiment dentaire fait mal ?</h3>
				<span class="faq-toggle">+</span>
			</div>
			<p class="faq-answer">Le blanchiment dentaire peut parfois entraîner une sensibilité dentaire temporaire, mais
				il ne devrait pas causer de douleur intense. Certaines personnes peuvent ressentir une légère gêne ou
				sensibilité pendant ou après le traitement de blanchiment. Cela peut se manifester par une sensibilité
				accrue aux aliments chauds ou froids, aux boissons sucrées ou acides, ou même à l'air froid. La sensibilité
				dentaire après un blanchiment est généralement de courte durée et disparaît souvent dans les jours qui
				suivent le traitement. Il est recommandé d'utiliser un dentifrice pour dents sensibles et d'éviter les
				aliments et les boissons chauds ou froids pendant un certain temps.</p>
		</div>

		<div class="faq-item">
			<div class="faq-header">
				<h3 class="faq-question">La durée d'un traitement orthodontique ?</h3>
				<span class="faq-toggle">+</span>
			</div>
			<p class="faq-answer">La durée d'un traitement orthodontique peut varier considérablement en fonction de
				plusieurs facteurs, tels que la complexité des problèmes orthodontiques, la méthode de traitement utilisée,
				la coopération du patient et la réponse individuelle aux traitements. Pour les cas orthodontiques mineurs,
				tels que des corrections mineures de l'alignement dentaire, un traitement peut durer de 6 à 12 mois.
				Cependant, pour des problèmes plus complexes, tels que des malocclusions sévères ou des mâchoires
				désalignées, le traitement peut prendre de 1 à 3 ans, voire plus dans certains cas.</p>
		</div>

		<div class="faq-item">
			<div class="faq-header">
				<h3 class="faq-question">La durée d'un traitement par facettes dentaires ?</h3>
				<span class="faq-toggle">+</span>
			</div>
			<p class="faq-answer">La durée d'un traitement par facettes dentaires peut varier en fonction de plusieurs
				facteurs, tels que le nombre de dents à traiter, l'état initial des dents, les techniques utilisées et les
				objectifs esthétiques spécifiques du patient. En général, le processus de pose de facettes dentaires peut
				être divisé en plusieurs étapes, qui peuvent s'étaler sur plusieurs semaines. Pour les cas orthodontiques
				mineurs, tels que des corrections mineures de l'alignement dentaire, un traitement peut durer de 6 à 12
				mois. Cependant, pour des problèmes plus complexes, tels que des malocclusions sévères ou des mâchoires
				désalignées, le traitement peut prendre de 1 à 3 ans, voire plus dans certains cas.</p>
